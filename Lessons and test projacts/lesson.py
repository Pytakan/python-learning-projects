import numpy as np

# создаём массив из чисел
arr = np.array([10, 20, 30, 40, 50])
print("Массив:", arr)

# арифметика сразу со всеми числами
print("Умножение на 3:", arr * 3)

# среднее значение
print("Среднее:", arr.mean())
# максимальное значение
print("Максимальное значение:", arr.max())  
# минимальное значение
print("Минимальное значение:", arr.min())

import pandas as pd

# создаём таблицу
data = {
    "Имя": ["Анна", "Иван", "Маша", "Пётр"],
    "Возраст": [25, 30, 22, 35],
    "Город": ["Киев", "Львов", "Харьков", "Одесса"]
}

df = pd.DataFrame(data)

print(df)  # вывод таблицы

print("Средний возраст:", df["Возраст"].mean())
print("Максимальный возраст:", df["Возраст"].max())
print("Минимальный возраст:", df["Возраст"].min())

import matplotlib.pyplot as plt

df["Возраст"].plot(kind="bar", title="Возраст людей")
plt.show()
